package com.fks.ui.master.vo;

/**
 *
 * @author krutij
 */
public class EventTypeVO {
    private  Long eventId;
    private String eventName;
    private Short isBlocked;

    public EventTypeVO(Long eventId, String eventName, Short isBlocked) {
        this.eventId = eventId;
        this.eventName = eventName;
        this.isBlocked = isBlocked;
    }

    public EventTypeVO() {
    }

   
    public Long getEventId() {
        return eventId;
    }

    public void setEventId(Long eventId) {
        this.eventId = eventId;
    }

    public String getEventName() {
        return eventName;
    }

    public void setEventName(String eventName) {
        this.eventName = eventName;
    }

    public Short getIsBlocked() {
        return isBlocked;
    }

    public void setIsBlocked(Short isBlocked) {
        this.isBlocked = isBlocked;
    }
    

}
