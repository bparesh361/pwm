package com.fks.promo.service;

import java.io.Serializable;

/**
 *
 * @author krutij
 */
public class CommonPromoVo implements Serializable {
    private String id;
    private String name;

    public CommonPromoVo(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public CommonPromoVo() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    

}
