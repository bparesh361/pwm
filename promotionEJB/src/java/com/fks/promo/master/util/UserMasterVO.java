package com.fks.promo.master.util;

import com.fks.promo.entity.MstEmployee;

/**
 *
 * @author krutij
 */
public class UserMasterVO {
    private  MstEmployee employee;
     private boolean isredirect;

    public UserMasterVO(MstEmployee employee, boolean isredirect) {
        this.employee = employee;
        this.isredirect = isredirect;
    }

    public UserMasterVO() {
    }

    
    public MstEmployee getEmployee() {
        return employee;
    }

    public void setEmployee(MstEmployee employee) {
        this.employee = employee;
    }

    public boolean isIsredirect() {
        return isredirect;
    }

    public void setIsredirect(boolean isredirect) {
        this.isredirect = isredirect;
    }

}
