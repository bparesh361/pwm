package com.fks.promo.master.vo;

import com.fks.promo.common.Resp;
import java.util.List;

/**
 *
 * @author ajitn
 */
public class UserDepartmentResponse {

    private Resp resp;
    private List<DepartmentVO> deptList;

    public UserDepartmentResponse() {
    }

    public UserDepartmentResponse(Resp resp) {
        this.resp = resp;
    }

    public UserDepartmentResponse(Resp resp, List<DepartmentVO> deptList) {
        this.resp = resp;
        this.deptList = deptList;
    }

    public List<DepartmentVO> getDeptList() {
        return deptList;
    }

    public void setDeptList(List<DepartmentVO> deptList) {
        this.deptList = deptList;
    }

    public Resp getResp() {
        return resp;
    }

    public void setResp(Resp resp) {
        this.resp = resp;
    }
}
