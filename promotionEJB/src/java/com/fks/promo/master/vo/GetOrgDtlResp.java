public class GetOrgDtlResp {
    private Resp resp;
    private StoreVO storeVO;

    public GetOrgDtlResp(Resp resp) {
        this.resp = resp;
    }

    public GetOrgDtlResp() {
    }

    public GetOrgDtlResp(Resp resp, StoreVO storeVO) {
        this.resp = resp;
        this.storeVO = storeVO;
    }

   

    public Resp getResp() {
        return resp;
    }

    public void setResp(Resp resp) {
        this.resp = resp;
    }

    public StoreVO getStoreVO() {
        return storeVO;
    }

    public void setStoreVO(StoreVO storeVO) {
        this.storeVO = storeVO;
    }
    

}
