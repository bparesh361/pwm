package com.fks.promo.init;


import com.fks.promo.common.Resp;
import com.fks.promotion.vo.PromotionVO;
import java.util.List;

/**
 *
 * @author Paresb
 */
public class SearchPromotionResp {
    
    private Resp resp;
    private List<PromotionVO> list;
    private Long totalCount;

    public SearchPromotionResp() {
    }

    public Long getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(Long totalCount) {
        this.totalCount = totalCount;
    }
    
    public SearchPromotionResp(Resp resp, List<PromotionVO> list) {
        this.resp = resp;
        this.list = list;
    }

    public SearchPromotionResp(Resp resp) {
        this.resp = resp;
    }
    
    
    

    public List<PromotionVO> getList() {
        return list;
    }

    public void setList(List<PromotionVO> list) {
        this.list = list;
    }

    public Resp getResp() {
        return resp;
    }

    public void setResp(Resp resp) {
        this.resp = resp;
    }   
    
    
}
