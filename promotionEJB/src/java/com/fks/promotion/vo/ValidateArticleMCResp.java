package com.fks.promotion.vo;

import com.fks.promo.common.Resp;
import java.util.List;

/**
 *
 * @author ajitn
 */
public class ValidateArticleMCResp {

    private Resp resp;
    private List<ValidateArticleMCVO> articleMCList;
    private String errorFilePath;

    public ValidateArticleMCResp() {
    }

    public ValidateArticleMCResp(Resp resp) {
        this.resp = resp;
    }

    public ValidateArticleMCResp(Resp resp, List<ValidateArticleMCVO> articleMCList, String errorFilePath) {
        this.resp = resp;
        this.articleMCList = articleMCList;
        this.errorFilePath = errorFilePath;
    }

    

    public List<ValidateArticleMCVO> getArticleMCList() {
        return articleMCList;
    }

    public void setArticleMCList(List<ValidateArticleMCVO> articleMCList) {
        this.articleMCList = articleMCList;
    }

    public Resp getResp() {
        return resp;
    }

    public void setResp(Resp resp) {
        this.resp = resp;
    }

    public String getErrorFilePath() {
        return errorFilePath;
    }

    public void setErrorFilePath(String errorFilePath) {
        this.errorFilePath = errorFilePath;
    }
}
