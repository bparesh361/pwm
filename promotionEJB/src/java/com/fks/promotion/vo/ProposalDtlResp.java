package com.fks.promotion.vo;

import com.fks.promo.common.Resp;

/**
 *
 * @author ajitn
 */
public class ProposalDtlResp {

    private Resp resp;
    private ProposalVO proposal;

    public ProposalDtlResp() {
    }

    public ProposalDtlResp(Resp resp) {
        this.resp = resp;
    }

    public ProposalDtlResp(Resp resp, ProposalVO proposal) {
        this.resp = resp;
        this.proposal = proposal;
    }

    public ProposalVO getProposal() {
        return proposal;
    }

    public void setProposal(ProposalVO proposal) {
        this.proposal = proposal;
    }

    public Resp getResp() {
        return resp;
    }

    public void setResp(Resp resp) {
        this.resp = resp;
    }
}
